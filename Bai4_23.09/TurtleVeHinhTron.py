#Ve hinh tron
from turtle import *

#Khoi tao doi tuong
turtle = Turtle()

#Dat kich thuoc cho vien hinh tron
turtle.pensize(5)

#Dat mau sac cho vien hinh tron la mau do
turtle.pencolor("red")

#Dat mau nen cho hinh tron la mau xanh
turtle.fillcolor("blue")
turtle.begin_fill()

#Dat ban kinh hinh tron
turtle.circle(100)
turtle.end_fill()

#Ket thuc
turtle.done()


